function decode(expr) {
  let b = expr
    .match(/\d{10}|\*{10}/g)
    .map((x) =>
      x == "**********"
        ? " "
        : x.replace(/10/g, ".").replace(/11/g, "-").replace(/00/g, "")
    );
  console.log(b);
  return b.map((x) => (MORSE_TABLE[x] ? MORSE_TABLE[x] : " ")).join("");
}

